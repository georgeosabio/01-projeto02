        # 07 - Crie um programa que calcule a média de dois números flutuantes fornecidos pelo usuário.

numero_01 = int(input("Digite o primeiro numero: "))
numero_02 = int(input("Digite o segundo numero: "))

resultado = (numero_01 + numero_02) / 2

print(f"A Média dos dois números é: ", resultado)